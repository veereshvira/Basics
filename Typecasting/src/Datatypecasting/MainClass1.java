	package Datatypecasting;

public class MainClass1 {

	public static void main(String[] args) {
		// type casting
		int x = (int)59.99;
		// double type casted to int
		double y = (double)100;
		//int type casted to double
		
		System.out.println(x);
		System.out.println(y);
		System.out.println("==========");
		int x1=125;
		double y1=546.949;
		
		int x2 = (int)y1;
		double y2 =(double)x1;
		System.out.println(x2);
		System.out.println(y2);
		
	}

}
